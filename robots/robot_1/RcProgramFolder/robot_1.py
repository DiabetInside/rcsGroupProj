def move_to_position(position_name, speed_x=100.0, speed_y=100.0, speed_z=0.0, tool="grab"):
    cart_ptp(position_name, speed_x, speed_y, speed_z, tool)

def grab_box():
    set_do("grab", True)
    wait_msec(GRAB_DELAY)

def release_box():
    set_do("grab", False)
    wait_msec(GRAB_DELAY)

while layer < LAYER_COUNT:
    while row < ROW_COUNT:
        while column < COLUMN_COUNT:
            # Перемещение к точке зщш
            move_to_position("origin", 500, 100, 0, "grab")
            
            wait_di("sensor", False, 500)
            
            # Получение текущей точки и корректировка координат для текущего столбца, строки и слоя
            point = load_cartesian("from", True).clone()
            point.x -= row * (WIDTH + GAP_SIZE)
            point.y -= column * (LENGTH + GAP_SIZE)
            point.z -= layer * HEIGHT

            # Перемещение к коробке
            line(point, 800, 1500, 0, "grab")
            grab_box()  # Захват коробки
            
            move_to_position("origin", 500, 100, 0, "grab")
            
            # Перемещение к точке сброса
            move_to_position("hover", 500, 100, 0, "grab")
            move_to_position("drop", 500, 100, 0, "grab")
            release_box()  # Сброс коробки
            move_to_position("hover", 500, 100, 0, "grab")
            
            column += 1  # Переход к следующей колонке
            
        column = 0  # Сброс колонки после завершения ряда
        row += 1  # Переход к следующему ряду
        
    row = 0  # Сброс ряда после завершения слоя
    layer += 1  # Переход к следующему слою

# Завершение работы и перемещение в точку ожидания
move_to_position("origin", 500, 100, 0, "grab")
